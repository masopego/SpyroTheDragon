package dam.pmdm.spyrothedragon.utils;

public class BubbleCoordinates {

    private float x;
    private float y;

    public BubbleCoordinates(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
